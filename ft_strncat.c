#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	unsigned int i;
	unsigned int j;
	unsigned int len;

	j = 0;
	i = ft_strlen(s1);
	len = ft_strlen(s1) + n;
	while (i < len && s2[j] != '\0')
	{
		s1[i] = s2[j];
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
