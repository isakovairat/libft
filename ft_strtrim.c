#include "libft.h"

char		*ft_strtrim(char const *s)
{
	size_t	len;
	char	*str;

	if (!s)
		return (NULL);
	while (*s == ' ' || *s == '\n' || *s == '\t')
		s++;
	len = ft_strlen(s);
	while (s[len - 1] == ' ' || s[len - 1] == '\n' || s[len - 1] == '\t')
		len--;
	str = ft_strndup(s, len);
	return (str);
}
